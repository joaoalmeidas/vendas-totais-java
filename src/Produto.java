
public class Produto {
	
	private int numero;
	private double valor;
	
	public Produto(int numero, double valor) {
		super();
		this.numero = numero;
		this.valor = valor;
	}
	
	public int getNumero() {
		return numero;
	}
	public void setNumero(int numero) {
		this.numero = numero;
	}
	public double getValor() {
		return valor;
	}
	public void setValor(double valor) {
		this.valor = valor;
	}
	
	
	
}
